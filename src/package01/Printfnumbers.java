package package01;

public class Printfnumbers {
  public static void main(String[] args) {
    int num = 42;
    System.out.printf("The value of num is %d\n", num); // Output: The value of num is 42

    float pi = 3.14159265359f;
    System.out.printf("The value of pi is approximately %.2f\n", pi); // Output: The value of pi is approximately 3.14

    int num1 = 8;
    System.out.printf("The octal representation of %d is %o\n", num1, num1); // Output: 10
  }
}
